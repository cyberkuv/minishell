#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int e;

	e = 0;
	if (!s || !f)
		return ;
	while (*s != '\0')
	{
		(*f)(e++, s++);
	}
}
