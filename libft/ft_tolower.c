#include "libft.h"

int		ft_tolower(int c)
{
	if (c >= 'A' && c <= 'Z')
		c = c + 32;
	return (c);
}

char	*ft_strlower(char *str)
{
	while (*str)
	{
		if (*str >= 65 && *str <= 90)
			*str += 32;
		str++;
	}
	return (str);
}
